package com.honey.user.service;

import com.honey.common.Common;

public class User extends Common{
	private String userId;
	private String userFullname;
	private String userPassword;
	private String userPhone;
	private String userEmail;
	private String userAddress;
	private String userRole;
	
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getUserFullname() {
		return userFullname;
	}
	public void setUserFullname(String userFullname) {
		this.userFullname = userFullname;
	}
	public String getUserPassword() {
		return userPassword;
	}
	public void setUserPassword(String userPassword) {
		this.userPassword = userPassword;
	}
	public String getUserPhone() {
		return userPhone;
	}
	public void setUserPhone(String userPhone) {
		this.userPhone = userPhone;
	}
	public String getUserEmail() {
		return userEmail;
	}
	public void setUserEmail(String userEmail) {
		this.userEmail = userEmail;
	}
	public String getUserAddress() {
		return userAddress;
	}
	public void setUserAddress(String userAddress) {
		this.userAddress = userAddress;
	}
	public String getUserRole() {
		return userRole;
	}
	public void setUserRole(String userRole) {
		this.userRole = userRole;
	}
	@Override
	public String toString() {
		return "User [userId=" + userId + ", userFullname=" + userFullname + ", userPassword=" + userPassword
				+ ", userPhone=" + userPhone + ", userEmail=" + userEmail + ", userAddress=" + userAddress
				+ ", userRole=" + userRole + ", toString()=" + super.toString() + "]";
	}
}
